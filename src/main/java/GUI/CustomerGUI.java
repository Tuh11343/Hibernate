    private void refresh()
    {
        loadCustomer();
        txtCustomerAddress.setText("");
        txtCustomerCity.setText("");
        txtCustomerID.setText("");
        txtSearch.setText("");
        txtCustomerName.setText("");
        txtCustomerPassword.setText("");
    }
    
    private void loadCustomer()
    {
        Object[][] data = customerBLL.getCustomer();
        if(data.length==0)
        {
            JOptionPane.showMessageDialog(this, "Customer Is Null Or Error Please Try Again");
            return;
        }
        String[] title = {"Customer ID","Full Name","Password","Address","City"};
        DefaultTableModel model = new DefaultTableModel(data, title);
        tblCustomer.setModel(model);
    }
    
    private void loadCustomerByID(int customerID)
    {
        Object[][] data= customerBLL.getCustomerByID(customerID);
        if(data.length==0)
        {
            JOptionPane.showMessageDialog(this, "Customer Is Null Or Error Please Try Again");
            return;
        }
        String[] title = {"Customer ID","Full Name","Password","Address","City"};
        DefaultTableModel model = new DefaultTableModel(data, title);
        tblCustomer.setModel(model);
    }
    
    private void loadCustomerByName(String customerName)
    {
        Object[][] data= customerBLL.getCustomerByName(customerName);
        if(data.length==0)
        {
            JOptionPane.showMessageDialog(this, "Customer Is Null Or Error Please Try Again");
            return;
        }
        String[] title = {"Customer ID","Full Name","Password","Address","City"};
        DefaultTableModel model = new DefaultTableModel(data, title);
        tblCustomer.setModel(model);
    }
    
    
    
