    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        int selectedRow = tblCustomer.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "There is no row selected");
            return;
        }
        int customerID = (int) tblCustomer.getModel().getValueAt(selectedRow, 0);
        if (customerBLL.deleteCustomer(customerID) == 0) {
            JOptionPane.showMessageDialog(this, "Delete Customer Success!");
        } else {
            JOptionPane.showMessageDialog(this, "Delete Customer Failed!");
        }

        refresh();
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       
         if(txtCustomerName.getText().length()==0||txtCustomerPassword.getText().length()==0||txtCustomerAddress.getText().length()==0
                 ||txtCustomerCity.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this, "Please fill out all the information!");
            return;
        }
        
        Customer customer=new Customer();
        customer.setAddress(txtCustomerAddress.getText());
        customer.setCity(txtCustomerCity.getText());
        customer.setFullName(txtCustomerName.getText());
        customer.setPassword(txtCustomerPassword.getText());
        
        if(customerBLL.addCustomer(customer)==0)
        {
            JOptionPane.showMessageDialog(this, "Add new Customer success");
        }else{
            JOptionPane.showMessageDialog(this, "Add new Customer failed");
        }
        refresh();
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMouseClicked
        int SelectedRow=tblCustomer.getSelectedRow();
        
        txtCustomerID.setText(String.valueOf(tblCustomer.getValueAt(SelectedRow, 0)));
        txtCustomerName.setText(String.valueOf(tblCustomer.getValueAt(SelectedRow, 1)));
        txtCustomerPassword.setText(String.valueOf(tblCustomer.getValueAt(SelectedRow, 2)));
        txtCustomerAddress.setText(String.valueOf(tblCustomer.getValueAt(SelectedRow, 3)));
        txtCustomerCity.setText(String.valueOf(tblCustomer.getValueAt(SelectedRow, 4)));
        
    }//GEN-LAST:event_tblCustomerMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        
        if (txtSearch.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill out the information at the search box");
            return;
        }
        
        //vegID
        if(cmbSearch.getSelectedIndex()==0)
        {
            if(!check.CheckNumber(txtSearch.getText().toString()))
            {
                JOptionPane.showMessageDialog(this, "Search format is incorrect please try again!");
                return;
            }
            loadCustomerByID(Integer.parseInt(txtSearch.getText()));
            
        }else if(cmbSearch.getSelectedIndex()==1)//cusName
        {
            loadCustomerByName(txtSearch.getText());
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        
        int selectedRow = tblCustomer.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "There is no row selected");
            return;
        }
        
        Customer customer=new Customer();
        customer.setCustomerID(Integer.parseInt(txtCustomerID.getText()));
        customer.setAddress(txtCustomerAddress.getText());
        customer.setCity(txtCustomerCity.getText());
        customer.setFullName(txtCustomerName.getText());
        customer.setPassword(txtCustomerPassword.getText());
        
        if (customerBLL.updateCustomer(customer) == 0) {
            JOptionPane.showMessageDialog(this, "Update Customer Success!");
        } else {
            JOptionPane.showMessageDialog(this, "Update Customer Failed!");
        }

        refresh();
        
    }//GEN-LAST:event_btnEditActionPerformed

